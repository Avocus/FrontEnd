// Função para buscar artigos específicos
export function searchLegalArticle(query: string): string[] {
  const results: string[] = [];
  const searchTerm = query.toLowerCase();
  
  // Busca na Constituição
  Object.entries(extendedLegalKnowledge.constituicaoFederal.direitosFundamentais).forEach(([, value]) => {
    if (typeof value === 'string' && value.toLowerCase().includes(searchTerm)) {
      results.push(`CF - ${value}`);
    } else if (typeof value === 'object' && value.texto?.toLowerCase().includes(searchTerm)) {
      results.push(`CF - ${value.texto}`);
    }
  });
  
  // Busca no Código Civil
  Object.values(extendedLegalKnowledge.codigoCivilCompleto).forEach(section => {
    Object.entries(section).forEach(([, value]) => {
      if (value.toLowerCase().includes(searchTerm)) {
        results.push(`CC - ${value}`);
      }
    });
  });
  
  // Busca no Código Penal
  Object.values(extendedLegalKnowledge.codigoPenalCompleto).forEach(section => {
    Object.entries(section).forEach(([, value]) => {
      if (value.toLowerCase().includes(searchTerm)) {
        results.push(`CP - ${value}`);
      }
    });
  });
  
  return results.slice(0, 5); // Limita a 5 resultados mais relevantes
}

interface JurisprudenceItem {
  sumula?: string;
  tema?: string;
  texto: string;
  area: string;
}

// Função para buscar jurisprudências
export function searchJurisprudence(area: string): JurisprudenceItem[] {
  const allJurisprudence = [
    ...extendedLegalKnowledge.jurisprudencias.stf,
    ...extendedLegalKnowledge.jurisprudencias.stj,
    ...extendedLegalKnowledge.jurisprudencias.tst
  ];
  
  return allJurisprudence.filter(j => j.area === area || area === 'todas');
}
